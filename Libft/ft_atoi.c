#include "libft.h"

int	ft_atoi(const char *str)
{
	long	i;
	char	sign;

	i = 0;
	sign = 1;
	while (((*str > 8) && (*str < 14)) || (*str == ' '))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while ((*str >= 48) && (*str <= 57) && *str)
	{
		i = (i * 10) + (*str - 48);
		str++;
	}
	return ((int)i * sign);
}
