#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	char	*aux;

	aux = dst;
	while (*src)
	{
		*dst = *src;
		dst++;
		src++;
	}
	*dst = *src;
	return (aux);
}
